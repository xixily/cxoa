package com.chaoxing.oa.entity.page.pub.hetong;

public class Salers {
	private Integer id;
	private String username;
	private String email;
	private String cellCoreEmail;
	private Double thisYear;
	private Double lastYear;
	private Double yingshou;
	
	public Integer getId() {
		return id;
	}
	public String getUsername() {
		return username;
	}
	public String getEmail() {
		return email;
	}
	public String getCellCoreEmail() {
		return cellCoreEmail;
	}
	public Double getThisYear() {
		return thisYear;
	}
	public Double getLastYear() {
		return lastYear;
	}
	public Double getYingshou() {
		return yingshou;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public void setCellCoreEmail(String cellCoreEmail) {
		this.cellCoreEmail = cellCoreEmail;
	}
	public void setThisYear(Double thisYear) {
		this.thisYear = thisYear;
	}
	public void setLastYear(Double lastYear) {
		this.lastYear = lastYear;
	}
	public void setYingshou(Double yingshou) {
		this.yingshou = yingshou;
	}
	
	
}
